export const getLanguage = (): Language => {
    return (localStorage.getItem('sea_language') as Language) || 'en';
};

export const setLanguage = (lang: Language): void => {
    localStorage.setItem('sea_language', lang);
};

export function useLanguage() {
    const [lang, setLangState] = useState<Language>(getLanguage);

    const changeLanguage = (newLang: Language) => {
        setLanguage(newLang);
        setLangState(newLang);
        window.dispatchEvent(new Event('languagechange'));
    };

    useEffect(() => {
        const handleLangChange = () => {
            setLangState(getLanguage());
        };
        window.addEventListener('languagechange', handleLangChange);
        return () => window.removeEventListener('languagechange', handleLangChange);
    }, []);

    const t = (key: string): string => {
        const dict = LOCALIZATION_DICTS[lang] || LOCALIZATION_DICTS.en;
        return dict[key] || LOCALIZATION_DICTS.en[key] || key;
    };

    return { lang, changeLanguage, t };
}
